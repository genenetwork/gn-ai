def hybrid_digest(query: str, memory: Any = None) -> str:
    async def _run():
        final_html = None
        async for event in hybrid_search(
            query, memory=memory, user_id=str(uuid.uuid4())
        ):
            source = event["source"]
            kind = event["kind"]
            content = event["content"]
            if source == "hybrid" and kind == "final":
                final_html = content
                break
        return final_html

    return _run_async(_run)
