def sparql_digest(
    query: str, handler: Any, memory: Any = None, user_id: str = "default_user"
) -> str:
    async def _run():
        parts = []
        async for chunk in handler(
            query=query,
            sparql_url=Config.SPARQL_ENDPOINT,
            memory=memory,
            user_id=user_id,
        ):
            parts.append(str(chunk))
        return "".join(parts)

    return _run_async(_run)


